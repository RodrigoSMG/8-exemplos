// Declaração das variáveis
let altura = 5;
let largura = 15;

// Cálculo da área
let area = altura * largura;
console.log("Área:", area);

// Cálculo do perímetro
let perimetro = 2 * (altura + largura);
console.log("Perímetro:", perimetro);

// Multiplicação
let multiplicacao = altura * largura;
console.log("Multiplicação:", multiplicacao);

// Divisão
let divisao = largura / altura;
console.log("Divisão:", divisao);

// Resto da divisão de largura por altura
let restoDivisao = largura % altura;
console.log("Resto da divisão:", restoDivisao);

// Cálculo da hipotenusa
let hipotenusa = Math.sqrt(Math.pow(altura, 2) + Math.pow(largura, 2));
console.log("Hipotenusa:", hipotenusa);
